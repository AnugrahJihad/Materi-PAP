print("TIPE-TIPE DATA")
print()

# 1. Integer
print("1. Integer")
# Integer adalah angka tanpa koma, baik positif maupun negatif
# Contoh: -5, -1, 0, 1, 5
a = 5
print(type(a))
print()

# 2. Float
print("2. Float")
# Float adalah angka dengan koma
# Contoh: 4.5, 5.6
b = 4.5
print(type(b))
print()

# 3. Boolean
print("3. Boolean")
# Boolean adalah tipe data yang menyatakan kebenaran: True atau False
print(type(True))
print(type(False))
print()

# 4. String
print("4. String")
# String adalah kumpulan karakter/huruf
c = "Anugrah Jihad Nur Ridho"
print(type(c))
print()

# 5. Variable
print("5. Variable")
# Variable adalah nama yang menyimpan nilai (objek)
n = 100
print(n)
print()

# 6. Type Casting (Float ke Integer)
print("6. Type Casting (float → int)")
d = int(4.9)  # hanya mengambil angka sebelum koma
print(d)
print()

# 7. Type Casting (Integer ke String)
print("7. Type Casting (int → str)")
e = str(45)
print(e)
print(type(e))
print()

# 8. Type Casting (String ke Integer)
print("8. Type Casting (str → int)")
f = int("456")
print(f)
print(type(f))
print()

# 9. Merubah String menjadi List
print("9. String → List")
g = list("Jihad")
print(g)
print(type(g))
print()
